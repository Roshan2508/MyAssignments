package week1.day2;

public class PostiveNorNegative {

	public static void main(String[] args) {
		int num=35;
		if (num<0) {
			System.out.println("The number is negative");
		}
		else if (num==0) {
			System.out.println("The number is neither postive nor negative");
		}
		else {
			System.out.println("The number is postive");
		}
			

	}

}
